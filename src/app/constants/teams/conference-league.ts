// 팀 ID로 UEFA 컨퍼런스 리그 팀 정보 가져오기
export function getConferenceLeagueTeamById(id: number): TeamMapping | undefined {
  return CONFERENCE_LEAGUE_TEAMS.find(team => team.id === id);
}

// 팀 이름으로 UEFA 컨퍼런스 리그 팀 검색하기 (한글/영문 모두 검색 가능)
export function searchConferenceLeagueTeamsByName(name: string): TeamMapping[] {
  const lowercaseName = name.toLowerCase();
  return CONFERENCE_LEAGUE_TEAMS.filter(team => 
    team.name_ko.includes(name) || 
    team.name_en.toLowerCase().includes(lowercaseName)
  );
} 