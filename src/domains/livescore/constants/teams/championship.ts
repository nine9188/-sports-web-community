// 팀 ID로 챔피언십 팀 정보 가져오기
export function getChampionshipTeamById(id: number): TeamMapping | undefined {
  return CHAMPIONSHIP_TEAMS.find(team => team.id === id);
}

// 팀 이름으로 챔피언십 팀 검색하기 (한글/영문 모두 검색 가능)
export function searchChampionshipTeamsByName(name: string): TeamMapping[] {
  const lowercaseName = name.toLowerCase();
  return CHAMPIONSHIP_TEAMS.filter(team => 
    team.name_ko.includes(name) || 
    team.name_en.toLowerCase().includes(lowercaseName)
  );
} 